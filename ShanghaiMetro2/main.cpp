/***************************************************************************
  文件名称：main.cpp
  功    能：应用程序主入口文件
  说    明：创建应用程序对象和主窗口，启动应用程序事件循环
***************************************************************************/

#include <QApplication>
#include "MainWindow.h"

/***************************************************************************
  函数名称：main
  功    能：应用程序主入口函数
  输入参数：int   argc - 命令行参数数量
            char* argv - 命令行参数数组
  返 回 值：int - 应用程序退出代码
  说    明：创建QApplication实例和主窗口，启动事件循环
***************************************************************************/
int main(int argc, char* argv[]) {
    QApplication app(argc, argv);

    MainWindow window;
    window.show();

    return app.exec();
}

